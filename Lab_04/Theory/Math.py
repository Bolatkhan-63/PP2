#Built-in Math Functions
x = min(5, 10, 25)
y = max(5, 10, 25)

print(x)
print(y)

print(abs(-7.25))  #return positive number
print(pow(4,3)) #return 4*4*4=64

#The Math Module
import math

x = math.sqrt(64) 
print(x)

y1 = math.ceil(1.4)
y2 = math.floor(1.4)

print(y1)  #return 2
print(y2)  #return 1

number_PI = math.pi
print(number_PI)

