cars = ["Ford", "Volvo", "BMW"]

x = len(cars)

print(x)

#You can use the append() method to add an element to an array.
cars.append("Honda")
print(cars)

#You can use the pop() method to remove an element from the array.
cars.pop(1)
print(cars)

#You can also use the remove() method to remove an element from the array.
cars.remove("BMW")
print(cars)

